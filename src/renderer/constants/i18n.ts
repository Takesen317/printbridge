export function translate(locale: AppLocale, key: string, params?: Record<string, string | number>): string {
  const template = messages[locale][key] ?? messages['en-US'][key] ?? key
  if (!params) return template
  return Object.entries(params).reduce((result, [paramKey, value]) => result.split(`{${paramKey}}`).join(String(value)), template)
}

